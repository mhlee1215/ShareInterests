package com.si.domain;

public class Category {
	
	private int		id				= 0;	// ID
	private String 	name			= "";

	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
